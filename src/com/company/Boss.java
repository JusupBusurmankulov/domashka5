package com.company;

public class Boss {
    private int bossHealth;
    private int bossDamage;
    private String bossTypeDefence;

    public int getBossHealth() {
        return bossHealth;
    }

    public void setBossHealth(int bossHealth) {
        this.bossHealth = bossHealth;
    }

    public int getBossDamage() {
        return bossDamage;
    }

    public void setBossDamage(int bossDamage) {
        this.bossDamage = bossDamage;
    }

    public String getBossTypeDefence() {
        return bossTypeDefence;
    }

    public void setBossTypeDefence(String bossTypeDefence) {
        this.bossTypeDefence = bossTypeDefence;
    }
}


